#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	char	*str2;

	str2 = (char *)str;
	while (*str2 != ch)
	{
		if (*str2++ == '\0')
			return (NULL);
	}
	return (str2);
}
