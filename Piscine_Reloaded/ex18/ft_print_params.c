void	ft_putchar(char c);

int		main(int ac, char **av)
{
	int j;
	int i;

	j = 0;
	i = 0;
	if (ac > 1)
	{
		while (av[++j])
		{
			while (av[j][i] != '\0')
				ft_putchar(av[j][i++]);
			i = 0;
			ft_putchar('\n');
		}
	}
	return (0);
}
